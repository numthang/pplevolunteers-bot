const MEDIA_TEAM_TRIGGERS = ['ทีมกราฟิก', 'ทีมช่างภาพ', 'ทีมคอนเทนต์', 'ทีมตัดต่อ'];
const MEDIA_TEAM_NAME    = 'ทีมสื่อ';

const DRY_RUN = process.argv.includes('--dry-run');

async function getRoleId(guildId, roleName) {
  const { rows } = await pool.query(
    'SELECT role_id FROM dc_guild_roles WHERE guild_id = $1 AND role_name = $2',
    [guildId, roleName]
  );
  return rows[0]?.role_id ?? null;
}

async function setParent(guildId, childName, parentName, label) {
  const [childId, parentId] = await Promise.all([
    getRoleId(guildId, childName),
    getRoleId(guildId, parentName),
  ]);
  if (!childId)  { console.log(`  ⚠️  ไม่พบ role: ${childName}`);  return false; }
  if (!parentId) { console.log(`  ⚠️  ไม่พบ role: ${parentName}`); return false; }
  if (DRY_RUN) {
    console.log(`  [DRY] ${label}: ${childName} → parent: ${parentName}`);
    return true;
  }
  await pool.query(
    'UPDATE dc_guild_roles SET parent_role_id = $1 WHERE guild_id = $2 AND role_id = $3',
    [parentId, guildId, childId]
  );
  return true;
}

async function main() {
  const { rows: guilds } = await pool.query('SELECT DISTINCT guild_id FROM dc_guild_roles');
  if (!guilds.length) { console.log('ไม่มี guild ใน dc_guild_roles'); process.exit(0); }

  for (const { guild_id } of guilds) {
    console.log(`\n🔧 Guild: ${guild_id}${DRY_RUN ? '  (DRY-RUN)' : ''}`);

    // 1. MEDIA_TEAM
    console.log('  📺 MEDIA_TEAM triggers → ทีมสื่อ');
    for (const triggerName of MEDIA_TEAM_TRIGGERS) {
      await setParent(guild_id, triggerName, MEDIA_TEAM_NAME, 'media');
    }

    // 2. Province → sub-region
    console.log('  🗺️  Province → sub-region');
    const provinceNames = Object.keys(SUB_REGION_MAP);
    let pOk = 0;
    for (const province of provinceNames) {
      const subRegion = SUB_REGION_MAP[province];
      // province role name = 'ทีม' + province (convention ที่ seed ใช้)
      const ok = await setParent(guild_id, 'ทีม' + province, subRegion, 'prov→sub');
      if (ok) pOk++;
    }
    console.log(`    → ${pOk}/${provinceNames.length} จังหวัด`);

    // 3. Sub-region → main-region
    console.log('  🗺️  Sub-region → main-region');
    const subRegions = [...new Set(Object.values(SUB_REGION_MAP))];
    let sOk = 0;
    for (const subRegion of subRegions) {
      // หา main region จาก province ตัวแรกที่ map ไป subRegion นี้
      const sampleProvince = Object.keys(SUB_REGION_MAP).find(p => SUB_REGION_MAP[p] === subRegion);
      const mainRegion = MAIN_REGION_MAP[sampleProvince];
      if (!mainRegion || mainRegion === subRegion) continue; // BKK sub = main (ทีมกรุงเทพ / ทีมปริมณฑล)
      const ok = await setParent(guild_id, subRegion, mainRegion, 'sub→main');
      if (ok) sOk++;
    }
    console.log(`    → ${sOk}/${subRegions.length} sub-regions`);
  }

  console.log('\n✅ เสร็จสิ้น');
  process.exit(0);
}

main().catch(e => { console.error(e); process.exit(1); });
