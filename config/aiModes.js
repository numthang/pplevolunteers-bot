const DEFAULT_MODE = 'summary';

function getMode(value) {
  return AI_MODES.find(m => m.value === value) ?? AI_MODES.find(m => m.value === DEFAULT_MODE);
}

module.exports = { AI_MODES, DEFAULT_MODE, getMode };
