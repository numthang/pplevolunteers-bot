function isAdmin(roles) {
  return roles.includes('Admin') || roles.includes('เลขาธิการ')
}

function hasProvinceScope(province, roles) {
  const provinceRole  = PROVINCE_ROLES[province]
  const subRegionRole = SUB_REGION_MAP[province]
  const mainRegionRole = MAIN_REGION_MAP[province]
  return roles.includes(provinceRole) || roles.includes(subRegionRole) || roles.includes(mainRegionRole)
}

/**
 * ตรวจสิทธิ์ดูบัญชี
 */
export function canViewAccount(account, discordId, roles = []) {
  const owner = account.owner_id === discordId

  if (account.visibility === 'private') return owner || isAdmin(roles)
  if (account.visibility === 'public')  return true

  // internal
  if (owner || isAdmin(roles)) return true

  if (account.province) {
    // ORG_ROLES ดูได้ถ้า scope จังหวัดตรง (ภาค/ภาคย่อย/จังหวัด)
    const hasOrgRole = ORG_ROLES.some(r => roles.includes(r))
    return hasOrgRole && hasProvinceScope(account.province, roles)
  } else {
    // province = null → คนในองค์กรทุก role ที่กำหนดดูได้
    return ORG_ROLES.some(r => roles.includes(r))
  }
}

/**
 * ตรวจสิทธิ์แก้ไข/ลบบัญชี
 */
export function canEditAccount(account, discordId, roles = []) {
  const owner = account.owner_id === discordId
  if (owner || isAdmin(roles)) return true

  if (account.province) {
    const hasTitle = PROVINCE_EDITOR_ROLES.some(r => roles.includes(r))
    return hasTitle && hasProvinceScope(account.province, roles)
  } else {
    // province = null → เหรัญญิกแก้ไขได้
    return roles.includes('เหรัญญิก')
  }
}

export function filterAccessibleAccounts(accounts, discordId, roles = []) {
  return accounts.filter(a => canViewAccount(a, discordId, roles))
}
