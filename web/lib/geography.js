// reverse index: role ภาค → [จังหวัด] (สร้างครั้งเดียวตอน load)
function invert(provinceToRegion) {
  const out = {}
  for (const [province, region] of Object.entries(provinceToRegion)) {
    ;(out[region] ||= []).push(province)
  }
  return out
}
export const PROVINCES_BY_SUB_REGION  = invert(SUB_REGION_MAP)
export const PROVINCES_BY_MAIN_REGION = invert(MAIN_REGION_MAP)

// ชั้น scope ที่แต่ละระบบ "รู้จัก" (SPEC §7) — finance รู้ครบ 3, calling ไม่รู้จัก region ใหญ่
const MODE_LEVELS = {
  finance: ['province', 'subregion', 'region'],
  calling: ['province', 'subregion'],
}

// 'province:ราชบุรี' → ['province', 'ราชบุรี'] · 'region:ทีมภาคกลาง' → ['region', 'ทีมภาคกลาง']
function splitGrant(grant) {
  const i = grant.indexOf(':')
  return i === -1 ? [grant, ''] : [grant.slice(0, i), grant.slice(i + 1)]
}

/**
 * แปลง raw scope grants → Set ของจังหวัดที่เข้าถึงได้ ตาม semantics ของแต่ละระบบ
 *
 * @param {string[]} grants  เช่น ['province:ราชบุรี', 'subregion:ทีมภาคกลางตะวันตก', 'region:ทีมภาคกลาง']
 * @param {{ mode: 'finance'|'calling' }} opts
 * @returns {Set<string>} จังหวัด — region grant จะถูก "มองข้าม" ใน mode calling
 */
export function expandGrants(grants = [], { mode } = {}) {
  const levels = MODE_LEVELS[mode]
  if (!levels) throw new Error(`expandGrants: unknown mode '${mode}'`)

  const provinces = new Set()
  for (const grant of grants) {
    const [type, value] = splitGrant(grant)
    if (!levels.includes(type)) continue
    if (type === 'province') {
      provinces.add(value)
    } else if (type === 'subregion') {
      PROVINCES_BY_SUB_REGION[value]?.forEach(p => provinces.add(p))
    } else if (type === 'region') {
      PROVINCES_BY_MAIN_REGION[value]?.forEach(p => provinces.add(p))
    }
  }
  return provinces
}
